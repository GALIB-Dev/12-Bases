const EmojiPicker = ({ onSelectEmoji, onClose }) => {
  const [activeCategory, setActiveCategory] = useState(0);
  const [searchTerm, setSearchTerm] = useState('');
  const [filteredEmojis, setFilteredEmojis] = useState([]);
  const pickerRef = useRef(null);

  // Close emoji picker when clicking outside of it
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        onClose();
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [onClose]);

  // Handle search functionality
  useEffect(() => {
    if (searchTerm.trim() === '') {
      setFilteredEmojis([]);
      return;
    }

    const results = [];
    EMOJI_CATEGORIES.forEach(category => {
      category.emojis.forEach(emoji => {
        if (emoji.includes(searchTerm)) {
          results.push(emoji);
        }
      });
    });

    setFilteredEmojis(results);
  }, [searchTerm]);

  const handleEmojiClick = (emoji) => {
    onSelectEmoji(emoji);
    
    // Add to frequently used (in a real app, this would be stored in localStorage or a database)
    console.log(`${emoji} selected`);
  };

  const renderEmojis = () => {
    if (searchTerm.trim() !== '') {
      return (
        <div className="emoji-grid">
          {filteredEmojis.map((emoji, index) => (
            <button
              key={`search-${index}`}
              className="emoji-button"
              onClick={() => handleEmojiClick(emoji)}
            >
              {emoji}
            </button>
          ))}
          {filteredEmojis.length === 0 && (
            <div className="no-results">No emojis found</div>
          )}
        </div>
      );
    }

    return (
      <div className="emoji-grid">
        {EMOJI_CATEGORIES[activeCategory].emojis.map((emoji, index) => (
          <button
            key={`cat-${activeCategory}-${index}`}
            className="emoji-button"
            onClick={() => handleEmojiClick(emoji)}
          >
            {emoji}
          </button>
        ))}
      </div>
    );
  };

  return (
    <div className="emoji-picker" ref={pickerRef}>
      <div className="emoji-picker-header">
        <input
          type="text"
          className="emoji-search"
          placeholder="Search emojis..."
          value={searchTerm}
          onChange={(e) => setSearchTerm(e.target.value)}
        />
      </div>
      
      {renderEmojis()}
      
      <div className="emoji-categories">
        {EMOJI_CATEGORIES.map((category, index) => (
          <button
            key={`category-${index}`}
            className={`category-button ${activeCategory === index ? 'active' : ''}`}
            onClick={() => {
              setActiveCategory(index);
              setSearchTerm('');
            }}
            title={category.name}
          >
            {category.emojis[0]}
          </button>
        ))}
      </div>
    </div>
  );
};

export default EmojiPicker; 